// -------------------------------------------------------------------------
// Proc12. Процедура впорядкування A, B, C за зростанням
// -------------------------------------------------------------------------
void SortInc3(double &A, double &B, double &C)
{
    // Переставляємо попарно – без циклів
    if (A > B) swap(A, B);
    if (B > C) swap(B, C);
    if (A > B) swap(A, B);
}

// -------------------------------------------------------------------------
// Proc45. Функція обчислення відстані d = |Ax + By + C| / sqrt(A^2 + B^2)
// -------------------------------------------------------------------------
double LinePointDistance(double x, double y, double A, double B, double C)
{
    return fabs(A * x + B * y + C) / sqrt(A * A + B * B);
}

